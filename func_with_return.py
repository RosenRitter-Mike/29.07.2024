def my_avg(a: int, b: int) -> float:
    """ return the average of two integers """
    return (a + b) / 2;


# b
def my_headline(s: str) -> str:
    """ function gets a string and return it in upper case and with exclamation sign as if shouting  """
    return s.upper()+"!";


